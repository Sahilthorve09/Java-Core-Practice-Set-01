
public class Operations {
	public static void main(String args[]) {
		/*Write a Java program to add, subtract, multiply, and divide two numbers entered by the user.
		  Write a program to find the remainder when one number is divided by another using the modulus (%) operator.
		*/
		int a = 20;
		int b = 10;
		System.out.println("Addition Of Two number: " + (a+b));
		System.out.println("Subtraction Of Two number: " + (a-b));
		System.out.println("Multiplication Of Two number: " + (a*b));
		System.out.println("Division Of Two number: " + (a/b));
		System.out.println("Mod Of Two number: " + (a%b));
	}
}
